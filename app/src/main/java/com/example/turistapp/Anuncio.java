package com.example.turistapp;

public class Anuncio {

    private String Titulo;
    private String Localidade;
    private String Informacao;
    private String DatadeEntrada;
    private String Preco;
    private String NrPessoas;
    private String Wifi;

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String titulo) {
        Titulo = titulo;
    }

    public String getLocalidade() {
        return Localidade;
    }

    public void setLocalidade(String localidade) {
        Localidade = localidade;
    }

    public String getInformacao() {
        return Informacao;
    }

    public void setInformacao(String informacao) {
        Informacao = informacao;
    }

    public String getDatadeEntrada() {
        return DatadeEntrada;
    }

    public void setDatadeEntrada(String datadeEntrada) {
        DatadeEntrada = datadeEntrada;
    }

    public String getPreco() {
        return Preco;
    }

    public void setPreco(String preco) {
        Preco = preco;
    }

    public String getNrPessoas() {
        return NrPessoas;
    }

    public void setNrPessoas(String nrPessoas) {
        NrPessoas = nrPessoas;
    }

    public String getWifi() {
        return Wifi;
    }

    public void setWifi(String wifi) {
        Wifi = wifi;
    }

    public String getAnimais() {
        return Animais;
    }

    public void setAnimais(String animais) {
        Animais = animais;
    }

    public String getVista() {
        return Vista;
    }

    public void setVista(String vista) {
        Vista = vista;
    }

    public String getAC() {
        return AC;
    }

    public void setAC(String AC) {
        this.AC = AC;
    }

    public String getFumadores() {
        return Fumadores;
    }

    public void setFumadores(String fumadores) {
        Fumadores = fumadores;
    }

    private String Animais;
    private String Vista;
    private String AC;
    private String Fumadores;
}
